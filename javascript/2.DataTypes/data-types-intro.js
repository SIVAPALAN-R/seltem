/*

Topic 4 - Data Types

Definition

A Data Type tells JavaScript what kind of value is stored in a variable.

Syntax

    let variableName = value;

Short Notes

-   Data Type tells JavaScript the type of stored data.
-   Every value in JavaScript has a Data Type.
-   Variables can store different types of data.
-   JavaScript supports multiple Data Types.
-   Common Data Types:
    -   String
    -   Number
    -   Boolean
    -   Undefined
    -   Null
    -   BigInt
    -   Symbol

------------------------------------------------------------------------

Type 1 - String

Definition

A String is a collection of characters (text) enclosed inside single (’
’) or double (” “) quotes.

Syntax

    let name = "Siva";

Short Notes

-   Stores text values.
-   Must be inside quotes.
-   Single or double quotes can be used.
-   Numbers inside quotes are also Strings.

Example:

    let age = "23";

------------------------------------------------------------------------

Type 2 - Number

Definition

A Number stores numeric values such as integers and decimal numbers.

Syntax

    let age = 23;

Short Notes

-   Stores numeric values.
-   Written without quotes.
-   Supports integers and decimal values.
-   Used for calculations.

------------------------------------------------------------------------

Type 3 - Boolean

Definition

A Boolean stores only two values: - true - false

Syntax

    let isStudent = true;

Short Notes

-   Stores only true or false.
-   No quotes.
-   Used in conditions, login, booking, validation.

------------------------------------------------------------------------

Type 4 - Undefined

Definition

A variable is declared but no value is assigned.

Syntax

    let city;

Short Notes

-   Variable created.
-   No value assigned.
-   JavaScript automatically gives undefined.

------------------------------------------------------------------------

Type 5 - Null

Definition

Represents an intentionally empty value.

Syntax

    let user = null;

Short Notes

-   Empty value assigned manually.
-   Different from Undefined.

------------------------------------------------------------------------

Type 6 - BigInt

Definition

Stores very large integer values.

Syntax

    let population = 12345678901234567890n;

Short Notes

-   Ends with n.
-   Used for very large integers.

------------------------------------------------------------------------

Type 7 - Symbol

Definition

Creates a unique value.

Syntax

    let id = Symbol();

Short Notes

-   Every Symbol is unique.
-   Used for unique identifiers.

------------------------------------------------------------------------

Summary

  Data Type   Example
  ----------- ------------
  String      “Siva”
  Number      25
  Boolean     true
  Undefined   let age;
  Null        null
  BigInt      123456789n
  Symbol      Symbol()
  */
 
