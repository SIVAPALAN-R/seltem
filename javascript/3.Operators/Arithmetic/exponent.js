/*
Definition:
The Exponent Operator (**) is used to raise a number to the power of another number.

Simple Definition:
The ** operator calculates the power of a number.

Short Notes

✔ ** is called the Exponent Operator.

✔ It calculates the power of a number.

✔ The first value is called the Base.

✔ The second value is called the Exponent (Power).

✔ It works only with numbers.

Syntax:
base ** exponent;

Example:
2 ** 3 = 8
*/

/* Exercise 1 */

let base = 2;
let power = 4;

console.log(base ** power);

