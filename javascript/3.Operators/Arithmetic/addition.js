/*📖 Addition Operator (+)
Definition

The Addition Operator (+) is used to add two or more numbers and return the total.

Simple Definition

The + operator is used to add values together.

Syntax
value1 + value2;

Example:

10 + 20;

Result:

30

🧠 How it Works
10 + 20
10  → Operand 1
+   → Addition Operator
20  → Operand 2
30  → Result

📝 Short Notes

✅ + is called the Addition Operator.

✅ It adds two or more numbers.

✅ It returns the total value.

✅ It works with numbers.

✅ It can also join strings (We'll learn this later under String Operators).*/


