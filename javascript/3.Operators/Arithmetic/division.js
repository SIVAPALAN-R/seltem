/*
Definition:
The Division Operator (/) is used to divide one number by another.

Simple Definition:
The / operator is used to divide values.

📝 Short Notes

✔ / is called the Division Operator.

✔ It divides one number by another.

✔ It returns the quotient.

✔ It works only with numbers.

Syntax:
value1 / value2;

Example:
20 / 4 = 5
*/

