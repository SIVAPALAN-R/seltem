/*
Definition:
The Multiplication Operator (*) is used to multiply two or more numbers.

Simple Definition:
The * operator is used to multiply values.

Short Notes

✔ * is called the Multiplication Operator.

✔ It multiplies two or more numbers.

✔ It returns the product.

✔ It works only with numbers.

Syntax:
value1 * value2;

Example:
10 * 5 = 50
*/

/*Exercise 1*/
