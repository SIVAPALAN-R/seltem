/*======================================

Topic 5 - Operators

======================================

Definition: An Operator is a special symbol that performs an operation
on one or more values (operands) and returns a result.

Simple Definition: Operator is a symbol used to perform an operation on
values or variables.

Syntax: value1 operator value2;

Example: 10 + 20

10 -> Operand + -> Operator 20 -> Operand Result -> 30

What is Operand? An Operand is the value on which an operator performs
an operation.

Short Notes:
 - Operators perform operations. 
 - Operators work with values and variables. 
- Operators always return a result. 
- Operators make calculations and comparisons easy.
 - Every JavaScript program uses operators.

Types of Operators: 
1. Arithmetic Operators (+ - * / % **) 
2. Assignment Operators (= += -= *= /= %=)
 3. Comparison Operators (== === != !== >< >= <=) 
4. Logical Operators (&& || !) 
5. Increment & Decrement (++, –)
6. String Operators (+, +=) 
7. Conditional (Ternary) Operator (? :) 
8.Type Operators (typeof, instanceof)

*/
