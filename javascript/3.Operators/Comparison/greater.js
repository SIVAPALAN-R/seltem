/* ======================================
   Topic 7.5 - Greater Than Operator (>)
====================================== */

/*
Definition:
The Greater Than Operator (>) compares two values.
If the left value is greater than the right value,
it returns true.
Otherwise, it returns false.

Simple Definition:
The > operator checks whether the left value is greater
than the right value.

Syntax:
value1 > value2;

Example:
console.log(20 > 10);   // true
console.log(10 > 20);   // false
*/


/* ======================================
   📝 Short Notes
====================================== */

/*
✔ > is called the Greater Than Operator.

✔ It compares two values.

✔ It returns true or false.

✔ The left value must be greater than the right value.

✔ Return type is Boolean.
*/

