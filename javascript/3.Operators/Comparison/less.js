/* ======================================
   Topic 7.6 - Less Than Operator (<)
====================================== */

/*
Definition:
The Less Than Operator (<) compares two values.
If the left value is less than the right value,
it returns true.
Otherwise, it returns false.

Simple Definition:
The < operator checks whether the left value is less
than the right value.

Syntax:
value1 < value2;

Example:
console.log(10 < 20);   // true
console.log(20 < 10);   // false
*/


/* ======================================
   📝 Short Notes
====================================== */

/*
✔ < is called the Less Than Operator.

✔ It compares two values.

✔ It returns true or false.

✔ The left value must be less than the right value.

✔ Return type is Boolean.
*/

