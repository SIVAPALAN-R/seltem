/* ======================================
   Topic 7.1 - Equal Operator (==)
====================================== */

/*
Definition:
The Equal Operator (==) is used to compare two values.
If both values are equal, it returns true.
Otherwise, it returns false.

Simple Definition:
The == operator checks whether two values are equal.

Short Notes:

✔ == is called the Equal Operator.

✔ It compares two values.

✔ It always returns a Boolean (true or false).

✔ It performs type conversion (type coercion) before comparing values.

✔ Mostly used in conditions.

Syntax:
value1 == value2;

Example:
console.log(10 == 10); // true
console.log(10 == 20); // false
*/

