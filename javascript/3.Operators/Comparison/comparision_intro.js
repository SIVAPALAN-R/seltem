/*
Definition:
Comparison Operators are used to compare two values.

Simple Definition:
Comparison Operators check whether two values are equal,
greater, or smaller.

They always return:
✔ true
✔ false

(Return Type: Boolean)

Syntax:
value1 operator value2;

Example:
console.log(10 > 5);   // true
console.log(20 == 10); // false

Short Notes:
✔ Used to compare two values.
✔ Always returns true or false.
✔ Return type is Boolean.
✔ Mostly used with if, else, loops, and conditions.

Types of Comparison Operators:

1. ==   Equal
2. ===  Strict Equal
3. !=   Not Equal
4. !==  Strict Not Equal
5. >    Greater Than
6. <    Less Than
7. >=   Greater Than or Equal
8. <=   Less Than or Equal
*/