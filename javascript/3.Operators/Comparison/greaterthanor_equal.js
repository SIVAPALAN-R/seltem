/* ======================================
   Topic 7.7 - Greater Than or Equal (>=)
====================================== */

/*
Definition:
The Greater Than or Equal (>=) operator compares two values.
If the left value is greater than or equal to the right value,
it returns true.
Otherwise, it returns false.

Simple Definition:
The >= operator checks whether the left value is greater than
or equal to the right value.

Syntax:
value1 >= value2;

Example:
console.log(20 >= 10);   // true
console.log(20 >= 20);   // true
console.log(10 >= 20);   // false
*/


/* ======================================
   📝 Short Notes
====================================== */

/*
✔ >= is called the Greater Than or Equal Operator.

✔ It compares two values.

✔ It returns true or false.

✔ It returns true if:
   - Left value is greater than the right value.
   - Left value is equal to the right value.

✔ Return type is Boolean.
*/

