/* ======================================
   Topic 9.1 - Increment Operator (++)
====================================== */

/*
Definition:
The Increment (++) operator increases
the value of a variable by 1.

Simple Definition:
The ++ operator adds 1 to a variable.

📝 Short Notes:
✔ ++ is called the Increment Operator.

✔ It increases a value by 1.

✔ It works only with variables.

✔ It is mostly used in loops and counters.

✔ Return type is Number.

Difference Table:

| Operator  | First Action       | Then           |
| --------- | ------------------ | -------------- |
| `count++` | Uses current value | Increases by 1 |
| `++count` | Increases by 1     | Uses new value |


Syntax:
variable++;

or

++variable;

Example:
let count = 5;
count++;

console.log(count);   // 6
*/
