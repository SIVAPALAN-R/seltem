/* ======================================
   Topic 9.3 - Pre Increment (++variable)
====================================== */

/*
Definition:
The Pre Increment (++variable) operator
first increases the value of a variable by 1,
then uses the updated value.

Simple Definition:
Pre Increment adds 1 first,
then prints the new value.

📝 Short Notes
✔ ++variable is called Pre Increment.

✔ It first increases the value.

✔ Then it uses the updated value.

✔ Mostly used in loops and counters.

✔ Return type is Number.

Syntax:
++variable;

Example:
let count = 5;

console.log(++count); // 6
console.log(count);   // 6
*/



/* Exercise 1 */

let counts = 10;

++counts;

console.log(counts);

/* Exercise 2 */

