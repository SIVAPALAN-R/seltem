/* ======================================
   Topic 13.1 - typeof Operator
====================================== */

/*
Definition:
The typeof operator is used to
find the data type of a variable
or value.

Simple Definition:
The typeof operator tells us
the type of data.

📖 Definition

The typeof operator returns
the data type of a variable
or value.

📝 Short Notes

✔ typeof is called the Type Operator.

✔ It returns the data type.

✔ The output is always a string.

✔ It works with variables and values.

✔ Common return values are:

   "string"
   "number"
   "boolean"
   "undefined"
   "object"
   "function"

Syntax:
typeof variable;

Example:

let name = "Siva";

console.log(typeof name);

// string
*/

/* Exercise 1 */

let studentName = "Siva";

console.log(typeof studentName);


