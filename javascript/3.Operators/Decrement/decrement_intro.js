/* ======================================
   Topic 10.1 - Decrement Operator (--)
====================================== */

/*
Definition:
The Decrement (--) operator decreases
the value of a variable by 1.

Simple Definition:
The -- operator subtracts 1 from a variable.

📝 Short Notes:
✔ -- is called the Decrement Operator.

✔ It decreases a value by 1.

✔ It works only with variables.

✔ It is mostly used in loops and counters.

✔ Return type is Number.

Syntax:
variable--;

or

--variable;

Example:
let count = 5;
count--;

console.log(count); // 4
*/

