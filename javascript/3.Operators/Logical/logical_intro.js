/* ======================================
   Topic 8 - Logical Operators
====================================== */

/*
Definition:
Logical Operators are used to combine or reverse
Boolean (true/false) values.

Simple Definition:
Logical Operators check multiple conditions and
return true or false.

They always return:
✔ true
✔ false

(Return Type: Boolean)

Syntax:
condition1 operator condition2;

Example:
console.log(true && true);   // true
console.log(true || false);  // true
console.log(!true);          // false

Short Notes:
✔ Used to combine two or more conditions.
✔ Always returns true or false.
✔ Return type is Boolean.
✔ Mostly used with if, else, loops, and conditions.

Types of Logical Operators:

1. &&   AND
2. ||   OR
3. !    NOT

&& → ALL conditions must be TRUE.

|| → ANY ONE condition is TRUE.

!  → Opposite of the Boolean value.

*/