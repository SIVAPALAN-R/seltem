/* ======================================
   Topic 6 - Assignment Operators
====================================== */

/*
Definition:
Assignment Operators are used to assign or update values in a variable.

Short Notes

✔ Assignment Operators are used to assign values to variables.

✔ They can also update the existing value of a variable.

✔ They make code shorter and easier to read.

✔ They work with variables.

Types of Assignment Operators:

| Operator | Meaning           | Example   |
| -------- | ----------------- | --------- |
| `=`      | Assignment        | `x = 10`  |
| `+=`     | Add & Assign      | `x += 5`  |
| `-=`     | Subtract & Assign | `x -= 5`  |
| `*=`     | Multiply & Assign | `x *= 5`  |
| `/=`     | Divide & Assign   | `x /= 5`  |
| `%=`     | Modulus & Assign  | `x %= 5`  |
| `**=`    | Exponent & Assign | `x **= 2` |



Syntax:
variable = value;

Example:
let age = 25;
age += 5;

Output:
30
*/