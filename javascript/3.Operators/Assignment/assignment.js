/*
Definition:
The Assignment Operator (=) is used to assign a value to a variable.

Simple Definition:
The = operator stores a value inside a variable.

📖 Definition

The Assignment Operator (=) is used to assign (store) a value in a variable.

📝 Short Notes

✔ = is called the Assignment Operator.

✔ It stores a value in a variable.

✔ It is used while creating variables.

✔ It works with let, var, and const.

✔ It assigns only one value at a time.

Syntax:
variable = value;

Example:
let age = 24;
*/


