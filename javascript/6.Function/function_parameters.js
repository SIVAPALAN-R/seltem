/* ======================================
   Topic - Parameters
====================================== */

/*
Definition:
A parameter is a variable
that receives a value
when a function is called.

Simple Definition:
A parameter is a variable
inside the function.

📖 Definition

Parameters are used
to receive data
from outside the function.

📝 Short Notes

✔ Declared inside
   parentheses ().

✔ Works like a variable.

✔ Receives values
   from arguments.

✔ Makes functions
   reusable.

✔ A function can have
   one or more parameters.
*/


/*
Exercise 1

Declare a function
named student.

Create one parameter
named name.

Inside the function,
print the parameter.

Do NOT call the function.

*/

function student(name){
   console.log(name);
}

/*
Exercise 2

Declare a function
named employee.

Create 3 parameters.

employeeId
employeeName
department

Print all parameters.

Do NOT call the function.

*/

function employee(employeeId, employeeName, department) {

    console.log(employeeId);
    console.log(employeeName);
    console.log(department);

}

