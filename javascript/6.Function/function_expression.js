/* ======================================
   Topic - Function Expression
====================================== */

/*
Definition:
A Function Expression
is a function
stored inside
a variable.

Simple Definition:
A function expression
is a function
assigned to
a variable.

📖 Definition

In a Function Expression,
a function is created
and stored
inside a variable.

The function
can be called
using the variable name.

📝 Short Notes

✔ Stored in a variable.

✔ Called using
   the variable name.

✔ Can have
   parameters.

✔ Can return values.

✔ Usually uses
   let or const.

✔ Function has
   no name
   (Anonymous Function).
*/


/*
Exercise 1

Create a variable
named welcome.

Store a function.

Print

"Welcome to JavaScript"

Call the function.

*/

let welcomes = function () {

    console.log("Welcome to JavaScript");

};

welcomes();


