/* ======================================
   Topic - Function Declaration
====================================== */

/*
Definition:
A function declaration
is the process of
creating a function
using the function keyword.

Simple Definition:
Function declaration
means creating
a function.

📖 Definition

A function declaration
defines a reusable block
of code that can be
called whenever needed.

📝 Short Notes

✔ Created using
   function keyword.

✔ Has a function name.

✔ Reusable.

✔ Executes only
   when called.

✔ Most common way
   to create a function.
*/

/*
Exercise 1

Declare a function
named welcome.

Inside the function,
print

"Welcome to JavaScript"

Do NOT call the function.

*/

function welcome() {

    console.log("Welcome to JavaScript");

}

/*
Exercise 2

Declare a function
named hotel.

Inside the function,
print

"Luxury Stay Hotel"

Do NOT call the function.

*/

