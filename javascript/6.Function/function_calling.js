/* ======================================
   Topic - Function Calling
====================================== */

/*
Definition:
Function calling is the process
of executing a function.

Simple Definition:
Function calling means
running the function.

📖 Definition

After creating a function,
we use its name followed
by parentheses ()
to execute the code
inside the function.

📝 Short Notes

✔ Executes the function.

✔ Uses function name.

✔ Parentheses () are required.

✔ Can call one function
   multiple times.

✔ Executes the code
   inside the function.
*/

/*
Exercise 1

Declare a function
named greeting.

Inside the function,
print

"Good Morning"

Call the function
one time.

*/

function greeting() {
    console.log("Good Morning");
}

greeting();


/*
Exercise 2

Declare a function
named course.

Inside the function,
print

"JavaScript"

Call the function
2 times.

*/

