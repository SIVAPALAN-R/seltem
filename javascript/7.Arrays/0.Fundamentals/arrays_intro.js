/*
Definition:
An Array is a special variable
used to store multiple values
in a single variable.

Simple Definition:
An Array stores many values
inside one variable.

📖 Definition

Instead of creating
many variables,

we can store
multiple values

inside one Array.

📝 Short Notes

✔ Stores multiple values.

✔ Uses square brackets [].

✔ Values are separated
   by commas (,).

✔ Index starts from 0.

✔ Can store
   strings,
   numbers,
   booleans,
   objects,
   and more.

Types of Arrays:

   1. String Array
   2. Number Array
   3. Boolean Array
   4. Mixed Array


Syntax:
let arrayName = [
    value1,
    value2,
    value3
];


Rules:
✅ Use []

✅ Separate values with ,

✅ Store multiple values

✅ One variable

❌ Don't use {}

❌ Don't use ()

*/