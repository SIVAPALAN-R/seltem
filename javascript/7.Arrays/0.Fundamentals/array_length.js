/* ======================================
   Topic - Array Length
====================================== */

/*
Definition:
The length property
returns the total number
of elements
inside an Array.

Simple Definition:
length tells us
how many values
are stored
inside an Array.

📖 Definition

Every Array
has a built-in property
called length.

It counts
all the elements
inside the Array.

📝 Short Notes

✔ length is a property.

✔ Returns total elements.

✔ Starts counting from 1.

✔ Index starts from 0.

✔ Syntax

arrayName.length
*/


/*
Exercise 1

Create a String Array.

Variable Name

courseNames

Values

HTML
CSS
JavaScript
React

Print

Total Courses

*/

let courseNames = [
    "HTML",
    "CSS",
    "JavaScript",
    "React"
];

console.log(courseNames.length);



