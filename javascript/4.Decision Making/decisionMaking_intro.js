/*
Definition:
Decision Making is used to execute
different blocks of code based on
a condition.

Simple Definition:
Decision Making helps JavaScript
make decisions.

📖 Definition

Decision Making allows a program
to check a condition and decide
which block of code should be executed.

If the condition is true,
one block of code runs.

If the condition is false,
another block of code may run.

📝 Short Notes

✔ Decision Making controls the program flow.

✔ It is based on conditions.

✔ Conditions return either:
   true
   or
   false

✔ Decision Making is used in
   Login Systems,
   Hotel Booking,
   ATM,
   E-commerce,
   Forms,
   Games, etc.

Types of Decision Making

1. if
2. if...else
3. else if
4. Nested if
5. switch
*/